package com.clases.springboot.app.model;

public class entTipoUsuario {
	
	private Integer tipUsu_Id;
	private String tipUsu_Codigo;
	private String tipUsu_Nombre;
	private String tipUsu_Descripcion;
	private Boolean tipUsu_Estado;
	public Integer getTipUsu_Id() {
		return tipUsu_Id;
	}
	public void setTipUsu_Id(Integer tipUsu_Id) {
		this.tipUsu_Id = tipUsu_Id;
	}
	public String getTipUsu_Codigo() {
		return tipUsu_Codigo;
	}
	public void setTipUsu_Codigo(String tipUsu_Codigo) {
		this.tipUsu_Codigo = tipUsu_Codigo;
	}
	public String getTipUsu_Nombre() {
		return tipUsu_Nombre;
	}
	public void setTipUsu_Nombre(String tipUsu_Nombre) {
		this.tipUsu_Nombre = tipUsu_Nombre;
	}
	public String getTipUsu_Descripcion() {
		return tipUsu_Descripcion;
	}
	public void setTipUsu_Descripcion(String tipUsu_Descripcion) {
		this.tipUsu_Descripcion = tipUsu_Descripcion;
	}
	public Boolean getTipUsu_Estado() {
		return tipUsu_Estado;
	}
	public void setTipUsu_Estado(Boolean tipUsu_Estado) {
		this.tipUsu_Estado = tipUsu_Estado;
	}
	
	
}
