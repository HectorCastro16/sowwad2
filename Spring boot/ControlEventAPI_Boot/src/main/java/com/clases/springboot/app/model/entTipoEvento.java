package com.clases.springboot.app.model;

public class entTipoEvento {

	
	private Integer tipEve_Id;
	
	private String tipEve_Codigo;
	
	private String tipEve_Nombre;
	
	private String tipEve_Descripcion;
	
	private Boolean tipEve_Estado;

	public Integer getTipEve_Id() {
		return tipEve_Id;
	}

	public void setTipEve_Id(Integer tipEve_Id) {
		this.tipEve_Id = tipEve_Id;
	}

	public String getTipEve_Codigo() {
		return tipEve_Codigo;
	}

	public void setTipEve_Codigo(String tipEve_Codigo) {
		this.tipEve_Codigo = tipEve_Codigo;
	}

	public String getTipEve_Nombre() {
		return tipEve_Nombre;
	}

	public void setTipEve_Nombre(String tipEve_Nombre) {
		this.tipEve_Nombre = tipEve_Nombre;
	}

	public String getTipEve_Descripcion() {
		return tipEve_Descripcion;
	}

	public void setTipEve_Descripcion(String tipEve_Descripcion) {
		this.tipEve_Descripcion = tipEve_Descripcion;
	}

	public Boolean getTipEve_Estado() {
		return tipEve_Estado;
	}

	public void setTipEve_Estado(Boolean tipEve_Estado) {
		this.tipEve_Estado = tipEve_Estado;
	}
	
	

	
}
